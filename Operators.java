package building.blocks.of.java;

public class Operators {

	public static void main(String[] args) {
		//Assignment Operator --> Used to assign values to the variables
				int a = 21;
				int b = 6;
				byte c = 127; //byte datatyoe
				short d = 130; //short datatype
				//boolean e = true;
				
				/*
				//Arithmetic Operators
				System.out.println(a+b);
				System.out.println(a-b);
				System.out.println(a*b);
				System.out.println(a/b);//returns quotient
				System.out.println(a%b);//returns remainder
				 */
				
				/*
				//Relational Operators --> Returns Boolean results
				System.out.println(a<b);
				System.out.println(a>b);
				System.out.println(a>=b);
				System.out.println(a<=b);
				System.out.println(a!=b);
				System.out.println(a==b);	
				*/
				
				//Logical Operators -->Returns Boolean results
				
				System.out.println(a>b && d<c);//both condition should be true, to return true 
				System.out.println(a>b || d<c);//anyone condition is true returns true
				
				
				/*
				//Bitwise Operators -->Converts into bit then performs operation and Returns Boolean results
				System.out.println(a>b & d<c); 
				System.out.println(a>b | d<c);
				*/
				
				/*
				//Shift Operators
				System.out.println(a<<b); //Shifts towards Left
				System.out.println(a>>b); //Shifts towards Right
				System.out.println(a>>>b);
				*/
				
				/*
				//Ternary Operator --> Like if..else statement
				System.out.println(a>b?a:b);
				System.out.println(d<c?c:d);
				*/
				
				/*
				//Unary Operators
			    	//-->Increment/Decrement
				//System.out.println(++a); // Prefix
				//System.out.println(a++);// Postfix
				//System.out.println(--a);
				//System.out.println(a--);
				
				    //-->Negating an expression
				//System.out.println(~a);
				     
				    //-->Inverting boolean values
				System.out.println(!e);
				
				*/
				

	}

}
